var number1 = Number(prompt('Informe o primeiro número: '))
var number2 = Number(prompt('Informe o segundo número: '))
var number3 = Number(prompt('Informe o terceiro número: '))

var media = (number1 + number2 + number3) / 3

alert(`A média dos números ${number1} ${number2} e ${number3}, é ${media}`)